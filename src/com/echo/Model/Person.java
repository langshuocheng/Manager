package com.echo.Model;

public class Person 
{
	private int pId;           //员工Id
	private String pName;      //姓名
	private String pSex;        //性别
	private int pAge;			//年龄
	private int pPost;			//职位
	private String pStartTime;  //入职时间
	private int pYear;			//工作年限
	
	
	
	
	
	public Person() {
		super();
	}
	
	
	public int getpId() {
		return pId;
	}
	public void setpId(int pId) {
		this.pId = pId;
	}
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public String getpSex() {
		return pSex;
	}
	public void setpSex(String pSex) {
		this.pSex = pSex;
	}
	public int getpAge() {
		return pAge;
	}
	public void setpAge(int pAge) {
		this.pAge = pAge;
	}
	public int getpPost() {
		return pPost;
	}
	public void setpPost(int pPost) {
		this.pPost = pPost;
	}
	public String getpStartTime() {
		return pStartTime;
	}
	public void setpStartTime(String pStartTime) {
		this.pStartTime = pStartTime;
	}
	public int getpYear() {
		return pYear;
	}
	public void setpYear(int pYear) {
		this.pYear = pYear;
	}
	
	

}
